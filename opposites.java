public class opposites {
    public static void main(String[] args) {
        int flower1 = 3;
        int flower2 = 4;
        if ((flower1 + flower2) % 2 == 1) {
            System.out.println("They love each other");
        } else {
            System.out.println("They don't love each other");
        }
    }
}
